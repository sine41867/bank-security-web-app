import cv2

class CameraHandler:
    def gen_frames():  
        camera = cv2.VideoCapture(0)  # Use 0 for web camera
        while True:
            success, frame = camera.read()
            if not success:
                break
            else:
                ret, buffer = cv2.imencode('.jpg', frame)
                frame = buffer.tobytes()
                yield (b'--frame\r\n'
                    b'Content-Type: image/jpeg\r\n\r\n' + frame + b'\r\n')